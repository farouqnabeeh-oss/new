async function run() {
  const { data: prods } = await supabase.from('products').select('*');

  function getBase(final) {
    for (let bp = final; bp <= final * 2; bp++) {
      if (Math.round(bp * 0.9) === final) return bp;
    }
    return Math.round(final / 0.9);
  }

  console.log('--- Updating Prices ---');
  for (const [uName, final] of Object.entries(userPrices)) {
    const dbName = map[uName] || uName;
    const p = prods.find(item => item.name_ar === dbName);
    if (p) {
      const base = getBase(final);
      await supabase.from('products').update({ base_price: base, discount: 10 }).eq('id', p.id);
    } else {
      // Double check for cases with extra/missing spaces or slight variations
      const p2 = prods.find(item => item.name_ar.includes(uName) || uName.includes(item.name_ar));
      if (p2) {
        const base = getBase(final);
        await supabase.from('products').update({ base_price: base, discount: 10 }).eq('id', p2.id);
      } else {
        console.log(`Product NOT FOUND: "${uName}"`);
      }
    }
  }

  console.log('\n--- Restricting Sizes ---');
  // Find classic cheeseburger
  const classic = prods.find(p => p.name_ar === 'كلاسيك تشيز بيرجر');
  if (classic) {
    // Find all groups with "الحجم" or product_id = null in category 1
    const { data: groups } = await supabase.from('addon_groups').select('*').eq('category_id', 1).eq('name_ar', 'الحجم');
    for (const g of groups) {
      await supabase.from('addon_groups').update({ product_id: classic.id }).eq('id', g.id);
      console.log(`Group ${g.id} restricted to ${classic.id}`);
    }
  }

  console.log('\nDone!');
}

run();
